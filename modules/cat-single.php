Piyasada pek çok farklı topaklanan kedi kumu çeşidi ve markası ile karşılaşmanız mümkün. Kokulu ve kokusuz gibi farklı estetik çözümlerin sunulduğu topaklanan kedi kumları, çoğunlukla granüllü şekilde temin edilir. Çoğu kedinin de kokusuz kedi kumunu tercih ettiğini belirtmekte fayda var; zira kokusuz olanlar daha doğal ve organik bir his sağlamaktadır. Bazı topaklanan kedi kumları, ıslandığında çimento-tarzı bir yapıya bürünerek tuvaletten çok daha kolay bir şekilde çıkartıldığı gibi çıkartılırken neredeyse hiç dağılmaz. Bu tip kedi kumlarını hiçbir koşulda tuvalete atmamalı ve üstüne sifon çekmemelisiniz. Bentonit kedi kumları olarak da bilinen bu kedi kumları, en sağlıklı kum çeşidi olarak bilinmekte ve çoğu kedi sahibi tarafından güvenle tercih edilmektedir.

